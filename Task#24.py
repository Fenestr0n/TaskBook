# Произвести циклический сдвиг списка на одну позицию влево (1 элемент на последнее место, 2 на 1, 3 на 2, и тд)
# N = 10
print("Введите 10 чисел")
a = [int(i) for i in input().split()]
print("Список до сдвига:")
print(a)
temp = a[0]
for i in range(1, 10):
    a[i - 1] = a[i]
a[9] = temp
print("Список после сдвига:")
print(a)